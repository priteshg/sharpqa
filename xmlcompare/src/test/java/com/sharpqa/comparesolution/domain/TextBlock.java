package com.sharpqa.comparesolution.domain;

public class TextBlock {
	String tag;
	String text;
	
	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}
	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}
}
